T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    n = int(input())
    num = [2, 3, 5, 7, 11]
    cnt = [0] * 5
    for i in range(5):
        while n % num[i] == 0 :
            cnt[i] += 1
            n //= num[i]
    print("#{}".format(test_case), end=" ")
    print(*cnt)
    # ///////////////////////////////////////////////////////////////////////////////////
